export type ToolIconName = keyof typeof ICONS

export function setToolIcon(el: HTMLElement, name: ToolIconName): void {
  el.innerHTML = ICONS[name]
}

export function createToolButton(
  className: string,
  icon: ToolIconName,
  title: string,
  onclick?: string,
): HTMLButtonElement {
  const btn = document.createElement('button')
  btn.type = 'button'
  btn.className = className
  btn.title = title
  btn.innerHTML = ICONS[icon]
  if (onclick) btn.setAttribute('onclick', onclick)
  return btn
}

export function flashCopied(el: HTMLElement, ms = 2000): void {
  setToolIcon(el, 'copied')
  setTimeout(() => setToolIcon(el, 'copy'), ms)
}
